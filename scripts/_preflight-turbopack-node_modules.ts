/**
 * Turbopack (Next 16) panics with `Symlink [project]/node_modules is invalid, it points out of
 * the filesystem root` when the top-level `node_modules` is a symlink pointing OUTSIDE the
 * project — the exact shape the box's spec-test worktree uses to share `REPO_DIR/node_modules`
 * cheaply (`scripts/builder-worker.ts` `ln -sfn REPO_DIR/node_modules <wt>/node_modules`).
 *
 * Node.js is happy with the symlink — it dereferences it during require() resolution — so the
 * `next` binary launches and loads `next.config.ts` normally through it. Turbopack panics later,
 * during its own project-fs scan (`try_get_next_package` in the reported call graph). This
 * side-effect module runs SYNCHRONOUSLY at the top of `next.config.ts`, BEFORE Turbopack ever
 * initializes, and materializes a real top-level `node_modules` in place:
 *
 *   1. `rm` the symlink
 *   2. `npm install --prefer-offline --no-audit --no-fund` — deterministic real tree matching
 *      THIS branch's `package-lock.json` exactly, ~20s from a warm npm cache. Chosen over
 *      `cp -al + npm install` because the two-step pattern races: Turbopack spawns worker
 *      Node processes (e.g. `[turbopack-node]/transforms/postcss.ts`) that each re-load
 *      `next.config.ts`, and a second npm install starting mid-materialize can strip packages
 *      the first pass hadn't finished linking. A single deterministic `npm install` is
 *      idempotent AND atomic — safe under concurrent worker load.
 *
 * No-op when node_modules is already a real directory, missing, or a symlink resolving INSIDE
 * the project. Idempotent — safe to run on every `next.config.ts` load (dev, build, type-check).
 *
 * Why this lives in the BRANCH (not just in `src/lib/spec-check-runner.ts`): the pre-merge
 * spec-test-runner in [[spec-check-runner]] is imported from `REPO_DIR/src/lib/spec-check-runner`
 * (main's copy — the box worker imports relative to its own checkout). A runner-side preflight
 * on the branch cannot take effect until the branch merges, so the pre-merge spec-test keeps
 * panicking. This branch-side preflight runs from files THIS branch owns, so the spec-test
 * against `origin/<branch>` sees it immediately.
 *
 * The runner-side preflight in `src/lib/spec-check-runner.ts` (`ensureRealTopLevelNodeModulesForBuild`)
 * is kept as the durable long-term fix for POST-merge spec-tests when the branch-side hook
 * isn't in the tree (e.g. a spec whose diff never touches next.config.ts).
 */
import { execSync, spawnSync } from "node:child_process";
import { lstatSync, readlinkSync } from "node:fs";
import { resolve as resolvePath } from "node:path";

function materializeIfPanicShape(): void {
  const repoRoot = process.cwd();
  const nmPath = resolvePath(repoRoot, "node_modules");
  let st;
  try {
    st = lstatSync(nmPath);
  } catch {
    return; // no node_modules — let next fail with its own clear error
  }
  if (!st.isSymbolicLink()) return; // already real — Turbopack accepts, subsequent loads skip fast
  let target: string;
  try {
    target = readlinkSync(nmPath);
  } catch {
    return; // unreadable — let downstream code surface the error
  }
  const resolved = resolvePath(repoRoot, target);
  if (resolved === repoRoot || resolved.startsWith(repoRoot + "/")) return; // in-project — accepted

  // Out-of-project symlink — the panic shape. Rm the symlink + fresh npm install for a
  // deterministic real tree matching THIS branch's package-lock. Skip when there is no
  // package.json (test sandboxes, non-npm projects) — just leaving the symlink removed is
  // enough to clear the specific Turbopack panic, and next will surface its own error.
  const shellQuote = (s: string) => `'${s.replace(/'/g, `'\\''`)}'`;
  try {
    execSync(`rm -f ${shellQuote(nmPath)}`, { stdio: "inherit" });
    let hasPkg = false;
    try {
      lstatSync(resolvePath(repoRoot, "package.json"));
      hasPkg = true;
    } catch { /* no package.json — skip */ }
    if (hasPkg) {
      // Strip inherited `npm_*` env vars — when `next build` runs under `npx next build`, npx
      // seeds `npm_config_*` / `npm_lifecycle_*` that could leak into the nested `npm install`.
      const cleanEnv = { ...process.env } as NodeJS.ProcessEnv;
      for (const k of Object.keys(cleanEnv)) {
        if (k.startsWith("npm_") || k.startsWith("NPM_")) delete cleanEnv[k];
      }
      // `--include=dev` — `next build` sets `NODE_ENV=production` in the parent process, which
      // npm treats as "omit devDependencies" by default. Many BUILD-time packages this project
      // needs (pg, @tailwindcss/postcss, tailwindcss, sharp, @types/*) live in devDependencies;
      // without this flag the tree comes up 250 packages short and Turbopack fails with
      // `Cannot find module '@tailwindcss/postcss'` / `Cannot resolve 'pg'`. Force the full
      // (deps + devDeps) install so the build tree matches the branch's lockfile exactly.
      execSync("npm install --include=dev --prefer-offline --no-audit --no-fund", {
        cwd: repoRoot,
        stdio: "inherit",
        env: cleanEnv,
      });
    }

    // RE-EXEC THIS NODE PROCESS. By the time next.config.ts loads, Node has already resolved
    // and cached many modules (`next` itself, jest-worker, postcss loaders, …) via the OLD
    // symlinked path — those cached paths point into the stale shared cache and NOT into the
    // fresh node_modules we just materialized. Turbopack then can't find e.g.
    // `@tailwindcss/postcss` because the worker probes the cached shared-cache path where it
    // doesn't exist. Restarting the process with a clean module cache resolves everything
    // fresh from our new real node_modules. spawnSync blocks; we forward the child's exit
    // code so the caller (`npx next build` → the runner) sees a single deterministic exit.
    const child = spawnSync(process.execPath, process.argv.slice(1), {
      cwd: repoRoot,
      stdio: "inherit",
      env: process.env,
    });
    process.exit(child.status ?? (child.signal ? 128 : 1));
  } catch (e) {
    // Do not throw from a side-effect import — Turbopack will surface its own panic if we can't
    // fix the tree, which preserves the original error signature the runner will classify.
    console.warn(`[preflight-turbopack-node_modules] materialize failed: ${(e as Error).message}`);
  }
}

materializeIfPanicShape();
